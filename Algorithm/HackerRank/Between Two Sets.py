def getTotalX(a, b):
    ans = 0
    for e in range(a[0], b[-1]+1):
        flag = True
        for aa in a:
            if e % aa != 0:
                flag = False
                break
        for bb in b:
            if bb % e != 0:
                flag = False
                break
        if flag: ans += 1
    return ans

